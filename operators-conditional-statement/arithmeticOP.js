// Arithmetic Operators

let a = 10;
let b = 5;

console.log("a =", a, "& b =", b);
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);

// Modulus --> remainder.
console.log("a % b =", a % b);

// Exponentiation --> symbol (**) two estores
console.log("a ** b", a ** b);

/* Unary operators
    Two Types of operators 1- Increment (++), 2- Decrement (--).
*/
// Increment -->
let x = 10;
let y = 5;

console.log("a =", a, "& b =", b);
x ++ // Increment 11
console.log("x =", x);

// Decrement -->
y --; // Decrement
console.log("y =",y);

/* Two type of increment and decrement -->
    1- post-increment (a++) 2- pre-increment (++a)
    1- post-decrement (a--) 2- pre-decrement (--a)
*/
// Pre-Increment
let i = 15
let j = 20
console.log("++i =", ++i);
