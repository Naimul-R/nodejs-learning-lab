// Arrow Function --> Compact way of writing a function. (=>)

// Sum function
function sum(a, b) {
    return a+b;
}

const arrowSum = (a, b) => {
    return a+b;
}

// Multiplication Function 
function mul(x, y) {
    console.log(x*y);
}

const arrowMul = (x, y) => {
    return x*y;
}


