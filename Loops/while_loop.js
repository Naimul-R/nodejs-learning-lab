/* While loop-->
    while (condition) {
        (do some work)
    }
*/
let name = 1;
while (name <= 8) {
    console.log("Naimul Rabby")
    name++
}

/* Do-While loop-->
    do {
        (do some work)
    } while (condition); 
*/
let i = 1;
do {
    console.log("Naimul");
    i++;
} while (i <= 5);